
public class Point {

	private int xValue;
	private int yValue;
	
	public Point(int x, int y){
		xValue = x;
		yValue = y;
	}

	public int getxValue() {
		return xValue;
	}

	public void setxValue(int xValue) {
		this.xValue = xValue;
	}

	public int getyValue() {
		return yValue;
	}

	public void setyValue(int yValue) {
		this.yValue = yValue;
	}
	
}
